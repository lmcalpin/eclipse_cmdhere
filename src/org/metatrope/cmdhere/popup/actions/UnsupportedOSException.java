package org.metatrope.cmdhere.popup.actions;

public class UnsupportedOSException extends RuntimeException {

	public UnsupportedOSException() {
		super();
	}

	public UnsupportedOSException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UnsupportedOSException(String arg0) {
		super(arg0);
	}

	public UnsupportedOSException(Throwable arg0) {
		super(arg0);
	}

}
